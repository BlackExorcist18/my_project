# 7. Fraction (рациональные дроби)
from fractions import Fraction

print("7. Операции с дробями:")
frac1 = Fraction(3, 4)
frac2 = Fraction(5, 6)

print(f"Дробь 1: {frac1}")
print(f"Дробь 2: {frac2}")
print(f"Сложение: {frac1 + frac2}")
print(f"Вычитание: {frac1 - frac2}")
print(f"Умножение: {frac1 * frac2}")
print(f"Деление: {frac1 / frac2}")